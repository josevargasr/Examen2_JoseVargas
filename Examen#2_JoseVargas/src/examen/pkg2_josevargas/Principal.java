    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String genero;
        if(cb_generot.getSelectedIndex()==0){
            genero = "Masculino";
        }else{
            genero = "Femenino";
        }
        Tecnico t = new Tecnico(tf_nomt.getText(), Integer.parseInt(tf_edadt.getText()), genero);
        administrarTecnicos at = new administrarTecnicos("./tecnicos.txt");
        at.cargarArchivo();
        at.getListatecnicos().add(t);
        try {
            at.escribirArchivo();
            JOptionPane.showMessageDialog(null, "Se creó el técnico con éxito");
            ((DefaultListModel) list_t.getModel()).clear();
            DefaultListModel modelo  = (DefaultListModel) list_t.getModel();
            for (Tecnico tec : at.getListatecnicos()) {
                modelo.addElement(tec);
            }
            list_t.setModel(modelo);
            jd_creart.dispose();
            tf_nomt.setText("");
            tf_edadt.setText("");
            cb_generot.setSelectedIndex(0);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Ocurrió un error al agregar al técnico");
        }
    }//GEN-LAST:event_jButton1MouseClicked

