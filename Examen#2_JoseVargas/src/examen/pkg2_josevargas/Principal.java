    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        boolean tactil = true;
        if(rb_tacno.isSelected()){
            tactil = false;
        }
        try {
            Ram r = new Ram(Integer.parseInt(tf_tamram.getText()), tf_marcaram.getText(), Integer.parseInt(tf_teram.getText()));
            Disco_Duro disc = new Disco_Duro(Integer.parseInt(tf_tamdisc.getText()), tf_marcadisco.getText(), Integer.parseInt(tf_tedisco.getText()));
            Bateria b = new Bateria(Integer.parseInt(tf_cap_horas.getText()), tf_matbateria.getText(), Integer.parseInt(tf_tebateria.getText()));
            Teclado t = new Teclado(tf_matteclado.getText(), jb_colortec.getBackground(), Integer.parseInt(tf_teteclado.getText()));
            Pantalla p = new Pantalla(tactil, tf_tipopantalla.getText(), Integer.parseInt(tf_tepantalla.getText()));
            Procesador pro = new Procesador(Integer.parseInt(tf_nucleos.getText()), Integer.parseInt(tf_velocidadp.getText()), Integer.parseInt(tf_teproce.getText()));
            Computadora c = new Computadora(Integer.parseInt(tf_serie.getText()), Integer.parseInt(tf_año.getText()), jb_colorcompu.getBackground(), tf_matcompu.getText(),r,disc,b,t,p,pro);
            adminCompus ac=new adminCompus("./compus.jfv");
            ac.cargarArchivo();
            ac.setCompu(c);
            ac.escribirArchivo();   
            JOptionPane.showMessageDialog(this, "Computadora guardada exitosamente");
            jd_crearc.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error al agregar la computadora");
        }
        
    }//GEN-LAST:event_jButton4MouseClicked

